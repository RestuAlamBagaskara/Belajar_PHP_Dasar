<?php

    //menghasilkan true jika perbandingan benar

    $a = 2;
    $b = "1";

    //operator == mengembalikan true jika nilai sama
    var_dump($a == $b);

    //operator === mengembalikan true jika nilai dan tipe sama
    var_dump($a === $b);

    //operator != atau <> mengembalikan true jika nilai tidak sama
    var_dump($a != $b);
    var_dump($a <> $b);

    //operator !== mengembalikan true jika nilai dan tipe tidak sama
    var_dump($a !== $b);

    //operator >= mengembalikan true jika nilai a lebih besar atau sama dengan dari b
    var_dump($a >= $b);

    //operator <= mengembalikan true jika nilai a lebih kecil atau sama dengan dari b
    var_dump($a <= $b);

    //operator > mengembalikan true jika nilai a lebih besar dari b
    var_dump($a > $b);

    //operator < mengembalikan true jika nilai a lebih kecil dari b
    var_dump($a < $b);
?>