<?php
    $a = 3;
    $b = 1;
    
    //$a += $b ==> $a = $a + $b
    $a += $b;
    echo $a . "\n";

    //$a -= $b ==> $a = $a - $b
    $a -= $b;
    echo $a . "\n";

    //$a *= $b ==> $a = $a * $b
    $a *= $b;
    echo $a . "\n";

    //$a /= $b ==> $a = $a / $b
    $a /= $b;
    echo $a . "\n";

    //$a **= $b ==> $a = $a ** $b
    $a **= $b;
    echo $a . "\n" ;

    //$a %= $b ==> $a = $a % $b
    $a %= $b;
    echo $a . "\n";
?>